const MAX_KID_LENGTH = 128;
const MAX_IDENTIFIER_LENGTH = 255;
const MAX_SCOPE_LENGTH = 128;
const MAX_HTU_LENGTH = 2048;
const MAX_KEYRING_KEYS = 32;
const MAX_KEY_BYTES = 4096;
const MAX_KEYRING_JSON_BYTES = 64 * 1024;
const SHA256_HEX_RE = /^[0-9a-f]{64}$/;
const BASE64URL_RE = /^[A-Za-z0-9_-]+$/;
const KID_RE = /^[A-Za-z0-9._-]+$/;
const PRINTABLE_ASCII_RE = /^[\x21-\x7e]+$/;
const UTF8_FATAL_DECODER = new TextDecoder('utf-8', { fatal: true });

export class InternalHmacError extends Error {
  /**
   * @param {string} code
   * @param {string} message
   */
  constructor(code, message) {
    super(message);
    this.name = 'InternalHmacError';
    this.code = code;
  }
}

function fail(code, message) {
  throw new InternalHmacError(code, message);
}

/**
 * @param {unknown} value
 * @returns {value is Record<string, unknown>}
 */
function isPlainObject(value) {
  if (value === null || typeof value !== 'object' || Array.isArray(value)) {
    return false;
  }
  const prototype = Object.getPrototypeOf(value);
  return prototype === Object.prototype || prototype === null;
}

/**
 * Reject symbols, non-enumerable properties and accessors as well as ordinary
 * missing/extra keys. This keeps the object accepted here identical to the
 * object that is serialized.
 *
 * @param {unknown} value
 * @param {readonly string[]} expected
 * @param {string} label
 * @returns {Record<string, unknown>}
 */
function assertExactObjectKeys(value, expected, label) {
  if (!isPlainObject(value)) {
    fail('INTERNAL_TOKEN_SCHEMA_INVALID', `${label} must be a plain object`);
  }

  const object = /** @type {Record<string, unknown>} */ (value);
  const ownKeys = Reflect.ownKeys(object);
  if (
    ownKeys.length !== expected.length ||
    ownKeys.some((key) => typeof key !== 'string' || !expected.includes(key))
  ) {
    fail(
      'INTERNAL_TOKEN_SCHEMA_INVALID',
      `${label} must contain exactly: ${expected.join(',')}`,
    );
  }

  for (const key of expected) {
    const descriptor = Object.getOwnPropertyDescriptor(object, key);
    if (
      !descriptor ||
      !descriptor.enumerable ||
      !Object.hasOwn(descriptor, 'value')
    ) {
      fail(
        'INTERNAL_TOKEN_SCHEMA_INVALID',
        `${label}.${key} must be an enumerable data property`,
      );
    }
  }
  return object;
}

/**
 * @param {unknown} value
 * @param {string} field
 * @param {number} maxLength
 * @returns {string}
 */
function assertAsciiIdentifier(
  value,
  field,
  maxLength = MAX_IDENTIFIER_LENGTH,
) {
  if (
    typeof value !== 'string' ||
    value.length === 0 ||
    value.length > maxLength ||
    !PRINTABLE_ASCII_RE.test(value)
  ) {
    fail(
      'INTERNAL_TOKEN_CLAIM_INVALID',
      `${field} must be 1..${maxLength} printable ASCII characters`,
    );
  }
  return value;
}

/**
 * @param {unknown} value
 * @param {string} field
 * @returns {number}
 */
function assertPositiveSafeInteger(value, field) {
  if (
    typeof value !== 'number' ||
    !Number.isSafeInteger(value) ||
    value <= 0
  ) {
    fail(
      'INTERNAL_TOKEN_CLAIM_INVALID',
      `${field} must be a positive safe integer number`,
    );
  }
  return value;
}

/**
 * Strict unpadded canonical base64url decoder.
 *
 * @param {unknown} value
 * @param {string} field
 * @returns {Buffer}
 */
function decodeCanonicalBase64url(value, field) {
  if (
    typeof value !== 'string' ||
    value.length === 0 ||
    !BASE64URL_RE.test(value) ||
    value.length % 4 === 1
  ) {
    fail(
      'INTERNAL_TOKEN_BASE64URL_INVALID',
      `${field} must be canonical unpadded base64url`,
    );
  }
  const decoded = Buffer.from(value, 'base64url');
  if (decoded.toString('base64url') !== value) {
    fail(
      'INTERNAL_TOKEN_BASE64URL_INVALID',
      `${field} must be canonical unpadded base64url`,
    );
  }
  return decoded;
}

/**
 * Parse a flat JSON object whose keys and values are strings, preserving the
 * ability to reject duplicate keys (JSON.parse alone would silently replace
 * them).
 *
 * @param {string} source
 * @returns {Record<string, string>}
 */
function parseJsonKeyring(source) {
  if (Buffer.byteLength(source, 'utf8') > MAX_KEYRING_JSON_BYTES) {
    fail('INTERNAL_TOKEN_KEYRING_INVALID', 'keyring JSON is too large');
  }

  let offset = 0;
  const result = Object.create(null);

  const skipWhitespace = () => {
    while (
      offset < source.length &&
      (source[offset] === ' ' ||
        source[offset] === '\t' ||
        source[offset] === '\r' ||
        source[offset] === '\n')
    ) {
      offset += 1;
    }
  };

  const parseString = () => {
    if (source[offset] !== '"') {
      fail(
        'INTERNAL_TOKEN_KEYRING_INVALID',
        'keyring JSON keys and values must be strings',
      );
    }
    const start = offset;
    offset += 1;
    let escaped = false;
    while (offset < source.length) {
      const code = source.charCodeAt(offset);
      const character = source[offset];
      if (!escaped && character === '"') {
        offset += 1;
        try {
          return JSON.parse(source.slice(start, offset));
        } catch {
          fail('INTERNAL_TOKEN_KEYRING_INVALID', 'invalid keyring JSON string');
        }
      }
      if (!escaped && code < 0x20) {
        fail('INTERNAL_TOKEN_KEYRING_INVALID', 'invalid keyring JSON string');
      }
      if (!escaped && character === '\\') {
        escaped = true;
      } else {
        escaped = false;
      }
      offset += 1;
    }
    fail('INTERNAL_TOKEN_KEYRING_INVALID', 'unterminated keyring JSON string');
  };

  skipWhitespace();
  if (source[offset] !== '{') {
    fail('INTERNAL_TOKEN_KEYRING_INVALID', 'keyring JSON must be an object');
  }
  offset += 1;
  skipWhitespace();
  if (source[offset] === '}') {
    offset += 1;
  } else {
    while (offset < source.length) {
      const key = parseString();
      if (Object.hasOwn(result, key)) {
        fail(
          'INTERNAL_TOKEN_KEYRING_INVALID',
          `duplicate keyring kid: ${key}`,
        );
      }
      skipWhitespace();
      if (source[offset] !== ':') {
        fail('INTERNAL_TOKEN_KEYRING_INVALID', 'invalid keyring JSON object');
      }
      offset += 1;
      skipWhitespace();
      const value = parseString();
      result[key] = value;
      skipWhitespace();
      if (source[offset] === '}') {
        offset += 1;
        break;
      }
      if (source[offset] !== ',') {
        fail('INTERNAL_TOKEN_KEYRING_INVALID', 'invalid keyring JSON object');
      }
      offset += 1;
      skipWhitespace();
    }
  }
  skipWhitespace();
  if (offset !== source.length) {
    fail(
      'INTERNAL_TOKEN_KEYRING_INVALID',
      'unexpected data after keyring JSON object',
    );
  }
  return result;
}

/**
 * @param {unknown} input
 * @returns {Map<string, Buffer>}
 */
function decodeKeyring(input) {
  let object;
  if (typeof input === 'string') {
    object = parseJsonKeyring(input);
  } else {
    if (!isPlainObject(input)) {
      fail(
        'INTERNAL_TOKEN_KEYRING_INVALID',
        'keyring must be an object or JSON object string',
      );
    }
    object = /** @type {Record<string, unknown>} */ (input);
    for (const key of Reflect.ownKeys(object)) {
      if (typeof key !== 'string') {
        fail(
          'INTERNAL_TOKEN_KEYRING_INVALID',
          'keyring cannot contain symbol keys',
        );
      }
      const descriptor = Object.getOwnPropertyDescriptor(object, key);
      if (
        !descriptor ||
        !descriptor.enumerable ||
        !Object.hasOwn(descriptor, 'value') ||
        typeof descriptor.value !== 'string'
      ) {
        fail(
          'INTERNAL_TOKEN_KEYRING_INVALID',
          'keyring values must be enumerable string data properties',
        );
      }
    }
  }

  const entries = Object.entries(object);
  if (entries.length === 0 || entries.length > MAX_KEYRING_KEYS) {
    fail(
      'INTERNAL_TOKEN_KEYRING_INVALID',
      `keyring must contain 1..${MAX_KEYRING_KEYS} keys`,
    );
  }

  const decoded = new Map();
  for (const [kid, encoded] of entries) {
    if (
      kid.length === 0 ||
      kid.length > MAX_KID_LENGTH ||
      !KID_RE.test(kid)
    ) {
      fail(
        'INTERNAL_TOKEN_KEYRING_INVALID',
        `kid must match ${KID_RE} and be at most ${MAX_KID_LENGTH} characters`,
      );
    }
    const key = decodeCanonicalBase64url(encoded, `keyring.${kid}`);
    if (key.length < 32 || key.length > MAX_KEY_BYTES) {
      fail(
        'INTERNAL_TOKEN_KEYRING_INVALID',
        `keyring.${kid} must decode to 32..${MAX_KEY_BYTES} bytes`,
      );
    }
    decoded.set(kid, key);
  }
  return decoded;
}

/**
 * Validate a keyring and active signing kid without exposing a fallback key.
 *
 * @param {unknown} keyring
 * @param {unknown} activeKid
 * @returns {{activeKid: string, kids: readonly string[]}}
 */
export function validateInternalHmacKeyring(keyring, activeKid) {
  const decoded = decodeKeyring(keyring);
  const kid = assertAsciiIdentifier(activeKid, 'activeKid', MAX_KID_LENGTH);
  if (!KID_RE.test(kid) || !decoded.has(kid)) {
    fail(
      'INTERNAL_TOKEN_ACTIVE_KID_UNKNOWN',
      'activeKid must identify an existing keyring entry',
    );
  }
  return Object.freeze({
    activeKid: kid,
    kids: Object.freeze([...decoded.keys()]),
  });
}

/**
 * @param {unknown} value
 * @param {string} field
 * @returns {string}
 */
function assertSha256(value, field) {
  if (typeof value !== 'string' || !SHA256_HEX_RE.test(value)) {
    fail(
      'INTERNAL_TOKEN_CLAIM_INVALID',
      `${field} must be 64 lowercase hexadecimal characters`,
    );
  }
  return value;
}

/**
 * @param {unknown} value
 * @returns {string}
 */
function assertHtu(value) {
  if (
    typeof value !== 'string' ||
    value.length === 0 ||
    value.length > MAX_HTU_LENGTH ||
    value[0] !== '/' ||
    !PRINTABLE_ASCII_RE.test(value) ||
    value.includes('?') ||
    value.includes('#')
  ) {
    fail(
      'INTERNAL_TOKEN_CLAIM_INVALID',
      `htu must be an ASCII absolute path without query/fragment and at most ${MAX_HTU_LENGTH} characters`,
    );
  }
  return value;
}

/**
 * @param {unknown} value
 * @returns {readonly [string]}
 */
function assertScope(value) {
  if (
    !Array.isArray(value) ||
    value.length !== 1 ||
    Reflect.ownKeys(value).some(
      (key) => key !== '0' && key !== 'length',
    )
  ) {
    fail(
      'INTERNAL_TOKEN_CLAIM_INVALID',
      'scope must contain exactly one element',
    );
  }
  return Object.freeze([
    assertAsciiIdentifier(value[0], 'scope[0]', MAX_SCOPE_LENGTH),
  ]);
}

/**
 * Validate and copy the exact final claim set in deterministic serialization
 * order.
 *
 * @param {unknown} input
 * @returns {Readonly<Record<string, unknown>>}
 */
export function validateInternalTokenClaims(input) {
  const claims = assertExactObjectKeys(
    input,
    INTERNAL_TOKEN_CLAIM_KEYS,
    'claims',
  );

  if (claims.token_version !== INTERNAL_TOKEN_VERSION) {
    fail(
      'INTERNAL_TOKEN_CLAIM_INVALID',
      `token_version must equal ${INTERNAL_TOKEN_VERSION}`,
    );
  }
  if (claims.request_hash_version !== 1) {
    fail(
      'INTERNAL_TOKEN_CLAIM_INVALID',
      'request_hash_version must equal 1',
    );
  }
  if (claims.iss !== INTERNAL_TOKEN_ISSUER) {
    fail('INTERNAL_TOKEN_CLAIM_INVALID', 'iss is invalid');
  }
  if (claims.aud !== INTERNAL_TOKEN_AUDIENCE) {
    fail('INTERNAL_TOKEN_CLAIM_INVALID', 'aud is invalid');
  }
  if (claims.sub !== INTERNAL_TOKEN_SUBJECT) {
    fail('INTERNAL_TOKEN_CLAIM_INVALID', 'sub is invalid');
  }

  const toolName = assertAsciiIdentifier(claims.tool_name, 'tool_name');
  const scope = assertScope(claims.scope);
  const isPreRunSessionEnsure =
    toolName === 'session.ensure' &&
    scope[0] === 'sandbox.sessions.ensure' &&
    claims.htu === '/internal/v1/sessions/ensure' &&
    claims.run_id === null &&
    claims.execution_fence_token === null;

  const normalized = {
    token_version: INTERNAL_TOKEN_VERSION,
    iss: INTERNAL_TOKEN_ISSUER,
    aud: INTERNAL_TOKEN_AUDIENCE,
    sub: INTERNAL_TOKEN_SUBJECT,
    org_id: assertAsciiIdentifier(claims.org_id, 'org_id'),
    user_id: assertAsciiIdentifier(claims.user_id, 'user_id'),
    conversation_id: assertAsciiIdentifier(
      claims.conversation_id,
      'conversation_id',
    ),
    agent_session_id: assertAsciiIdentifier(
      claims.agent_session_id,
      'agent_session_id',
    ),
    sandbox_session_id: assertAsciiIdentifier(
      claims.sandbox_session_id,
      'sandbox_session_id',
    ),
    run_id: isPreRunSessionEnsure
      ? null
      : assertAsciiIdentifier(claims.run_id, 'run_id'),
    tool_execution_id: assertAsciiIdentifier(
      claims.tool_execution_id,
      'tool_execution_id',
    ),
    tool_call_id: assertAsciiIdentifier(claims.tool_call_id, 'tool_call_id'),
    tool_name: toolName,
    scope,
    request_hash: assertSha256(claims.request_hash, 'request_hash'),
    request_hash_version: 1,
    execution_fence_token: isPreRunSessionEnsure
      ? null
      : assertPositiveSafeInteger(
          claims.execution_fence_token,
          'execution_fence_token',
        ),
    trace_id: assertAsciiIdentifier(claims.trace_id, 'trace_id'),
    htm: claims.htm,
    htu: assertHtu(claims.htu),
    body_sha256: assertSha256(claims.body_sha256, 'body_sha256'),
    iat: assertPositiveSafeInteger(claims.iat, 'iat'),
    nbf: assertPositiveSafeInteger(claims.nbf, 'nbf'),
    exp: assertPositiveSafeInteger(claims.exp, 'exp'),
    jti: claims.jti,
  };

  if (normalized.htm !== 'POST') {
    fail('INTERNAL_TOKEN_CLAIM_INVALID', 'htm must equal POST');
  }
  if (normalized.nbf !== normalized.iat) {
    fail('INTERNAL_TOKEN_CLAIM_INVALID', 'nbf must equal iat');
  }
  const ttl = normalized.exp - normalized.iat;
  if (ttl <= 0 || ttl > INTERNAL_TOKEN_MAX_TTL_SECONDS) {
    fail(
      'INTERNAL_TOKEN_CLAIM_INVALID',
      `exp-iat must be in 1..${INTERNAL_TOKEN_MAX_TTL_SECONDS}`,
    );
  }

  const jtiBytes = decodeCanonicalBase64url(normalized.jti, 'jti');
  if (jtiBytes.length !== 16) {
    fail('INTERNAL_TOKEN_CLAIM_INVALID', 'jti must encode exactly 128 bits');
  }
  normalized.jti = /** @type {string} */ (claims.jti);

  return Object.freeze(normalized);
}

