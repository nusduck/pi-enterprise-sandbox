/**
 * Short-lived Agent -> Sandbox internal tokens.
 *
 * This module intentionally implements only HS256 with a strict, closed
 * schema. It does not accept general JWT algorithms, permissive numeric
 * coercion, unknown claims, padded base64url, or an unknown-kid fallback.
 */

import {
  createHmac,
  randomBytes as cryptoRandomBytes,
  timingSafeEqual,
} from 'node:crypto';
import { TextDecoder } from 'node:util';

